# Programa galleta de la suerte
dia = input("¿Qué día es hoy? ")

# El if, elif y el else (alv)
if dia == "lunes":
    print("Hoy vas a ser grande")
elif dia == "martes":
    print("Todas tus metas se cumpliran")
elif dia == "miercoles":
    print("Tu puedes bro")
elif dia == "jueves":
    print("Ya eres grande solo falta confiar")
elif dia == "viernes":
    print("Eres el mejor")
elif dia == "sabado":
    print("Bro eres grande")
elif dia == "domingo":
    print("Tu sabes")
else:
    print("No existe ese día bro.")